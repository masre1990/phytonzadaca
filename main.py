x = input("How do you feel today?")

if x == "Happy":
    print("It is great to see you happy!")
elif x == "Nervous":
    print("Take a deep breath 3 times.")
elif x == "Sad":
    print("Eat some chocolate")
elif x == "Excited":
    print("Sing out loud.")
elif x == "Relaxed":
    print("Take a nap.")
else:
    print("I don't recognize this mood.")
